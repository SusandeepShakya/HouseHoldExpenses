public class Tenant {
    private int ID;
    private int TenantName;

    public Tenant() {
        this.TenantName = TenantName;
    }

    public int getID() {
        return ID;
    }
    public int getTenantName() {
        return TenantName;
    }
    public void setTenantName(int tenantName) {
        TenantName = tenantName;
    }
}
